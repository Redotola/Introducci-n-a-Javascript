// Operadores de comparacion
a = 5;
b = "5";
console.log(a);
console.log(b);

// Operadores igualdad ==
// solo compara valores y hace una conversion si es necesario

console.log('a == b -> ', a == b)

// Operadores estrictamente igualidad ===
// se compara el valor y el tipo de dato

console.log('a === b -> ', a === b)

// String Interpolation
console.log(`${a} === ${b} -> ${a === b}`);
console.log(`${a} == ${b} -> ${a == b}`)

// Operador distintos
// compara valor y convierte el tipo de dato si es necesario
console.log(`${a} != ${b} -> `, a != b);

// Operador distinto exacto
// compara el valor y el tipo de dato
console.log(`${a} !== ${b} -> , ${a !== b}`);

// Operador menor que
console.log(`${a} < ${b} -> ${a < b}`);

// Operador menor o igual que
console.log(`${a} <= ${b} -> ${a <= b}`);

// Operador mayor que
console.log(`${a} > ${b} -> ${a > b}`);

// Operador mayor o igual que
console.log(`${a} >= ${b} -> ${a >= b}`);

