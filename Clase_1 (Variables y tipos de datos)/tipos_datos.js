/*
- Tipos primitivos

Number -> Almacena valores numericos
String -> Almacena cadenas
Boolean -> Almacena valores logicos
Null -> Ausencia de la referencia a un objeto
Undefined -> Ausencia de valor
*/

let miEntero = 10;
console.log(miEntero);
console.log(typeof miEntero);

let miFlotante = 7.9;
console.log(miFlotante);
console.log(typeof miFlotante);

let miCadena = "Hola";
console.log(miCadena);
console.log(typeof miCadena);

let miBoolean = true;
console.log(miBoolean);
console.log(typeof miBoolean);

let miNulo = null;
console.log(miNulo);
console.log(typeof miNulo)

let miUndefined;
console.log(miUndefined);
console.log(typeof miUndefined);